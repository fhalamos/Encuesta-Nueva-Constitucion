'use strict';

angular.module('encuestaNuevaConstitucion')
  .factory('Question', function($resource) {
  return $resource("/api/questions/:id", { id: "@id" },
    {
      'create':  { method: 'POST' },
      'index':   { method: 'GET', isArray: true },
      'show':    { method: 'GET', isArray: false },
      'update':  { method: 'PUT' },
      'destroy': { method: 'DELETE' }
    }
  );
});
